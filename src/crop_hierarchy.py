# Reverse mapping: crop -> list of groups it belongs to
CROP_TO_GROUPS = {}
for group, crops in CROP_HIERARCHY.items():
    for crop in crops:
        if crop not in CROP_TO_GROUPS:
            CROP_TO_GROUPS[crop] = []
        CROP_TO_GROUPS[crop].append(group)

# All valid crop names as a set for quick lookup
ALL_CROPS = set()
for crops in CROP_HIERARCHY.values():
    ALL_CROPS.update(crops)


def get_crop_groups(crop_name: str) -> list:
    """Get list of groups a crop belongs to."""
    return CROP_TO_GROUPS.get(crop_name, [])


def is_valid_crop(crop_name: str) -> bool:
    """Check if a crop name is in the hierarchy."""
    return crop_name in ALL_CROPS


def get_crops_in_group(group_name: str) -> list:
    """Get list of crops belonging to a group."""
    return CROP_HIERARCHY.get(group_name, [])
