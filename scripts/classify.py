# Connect to DB
db = sqlite3.connect('data/reestr.db')
db.row_factory = sqlite3.Row
c = db.cursor()

# Load all tags into dict
c.execute('SELECT id, name, category FROM tags')
TAGS = {}
for row in c.fetchall():
    TAGS.setdefault(row['category'], {})[row['name']] = row['id']

def get_tag_id(category, name):
    return TAGS.get(category, {}).get(name)

def add_tag(category, name, description=''):
    c.execute('INSERT OR IGNORE INTO tags (name, category, description) VALUES (?, ?, ?)', (name, category, description))
    db.commit()
    c.execute('SELECT id FROM tags WHERE name = ? AND category = ?', (name, category))
    row = c.fetchone()
    if row:
        TAGS.setdefault(category, {})[name] = row[0]
        return row[0]
    return None

# Delete old crop_group tags before reclassification
c.execute("SELECT id FROM tags WHERE category = 'crop_group'")
old_crop_tag_ids = [row[0] for row in c.fetchall()]
if old_crop_tag_ids:
    placeholders = ','.join(['?' for _ in old_crop_tag_ids])
    c.execute(f"DELETE FROM product_tags WHERE tag_id IN ({placeholders})", old_crop_tag_ids)
    c.execute(f"DELETE FROM tags WHERE id IN ({placeholders})", old_crop_tag_ids)
    db.commit()
    print(f"Cleaned {len(old_crop_tag_ids)} old crop_group tags")

